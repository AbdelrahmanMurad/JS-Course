/*
  String Syntax + Character Escape Sequences
  \ Escape + Line Continue
  \n
*/

console.log('Elzero Web "School"');
console.log("Elzero Web 'School'");
console.log('Elzero Web \"School\"'); 
console.log("Elzero \\ Web \'School\'"); //Escape for backslash
console.log("Elzero \ Web \ School"); //Escape for nothing

console.log("Elzero \
Web \
School"); // nothing will happen
console.log("Elzero\nWeb\nSchool");//break

